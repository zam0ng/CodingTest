// 정수 리스트 num_list와 정수 n이 주어질 때, 
// num_list를 n 번째 원소 이후의 원소들과 n 번째까지의 원소들로 나눠 n 번째 원소 이후의 원소들을 n 번째까지의 원소들 앞에 붙인 리스트를 return하도록 solution 함수를 완성해주세요.
// num_list : [2, 1, 6],	     n :1	-> result : [1, 6, 2]
// num_list : [5, 2, 1, 7, 5],	 n :3	-> result : [7, 5, 5, 2, 1]

function solution(numArr, m) {
    var answer = [];
    // let numArr = [5, 2, 1, 7, 5];
    let newArr =[];
    let newArr2 =[];
    // let m = 3;

    for (let i = 0; i < numArr.length; i++) {
        
        if(i >= m){
            newArr.push(numArr[i]);
        }
        else{
            newArr2.push(numArr[i]);
        }
    }
    answer = [...newArr,...newArr2]
    return answer;
}
solution();

//다른 사람들의 풀이
function solution(num_list, n) {
    return [...num_list.slice(n), ...num_list.slice(0, n)];
}

